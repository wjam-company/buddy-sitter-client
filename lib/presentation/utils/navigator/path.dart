  static BuddySitterPath parse(String? url) {
    final Uri uri = Uri.parse(url ?? '');
    // path: /
    if (uri.pathSegments.isEmpty) {
      return BuddySitterPath.splashArt();
    }
    // path: /signin
    if (uri.pathSegments.first == 'signin') {
      return BuddySitterPath.singin();
    }
    // path: /explore
    if (uri.pathSegments.first == 'explore') {
      return BuddySitterPath.explore();
    }
    // path: /onboarding
    if (uri.pathSegments.first == 'onboarding') {
      return BuddySitterPath.onboarding();
    }
    // path: /signup
    if (uri.pathSegments.first == 'signup') {
      return BuddySitterPath.signup();
    }
    // path: /home
    if (uri.pathSegments.first == 'home') {
      return BuddySitterPath.home();
    }
    // path: /recover-password
    if (uri.pathSegments.first == 'recover-password') {
      return BuddySitterPath.recoverPassword();
    }
    // path: /select-your-pet
    if (uri.pathSegments.first == 'select-your-pet') {
      return BuddySitterPath.selectYourPet();
    }
    // path: /select-your-service
    if (uri.pathSegments.first == 'select-your-service') {
      return BuddySitterPath.selectYourService();
    }
    // path: /result-sitters
    if (uri.pathSegments.first == 'result-sitters') {
      return BuddySitterPath.resultSitters();
    }
    if (uri.pathSegments.first == 'schedule') {
      return BuddySitterPath.schedule();
    }
    if (uri.pathSegments.first == 'sitter-schedule') {
      return BuddySitterPath.sitterSchedule();
    }
    if (uri.pathSegments.first == 'pending-services') {
      return BuddySitterPath.pendingServices();
    }
    if (uri.pathSegments.first == 'sitter-services') {
      return BuddySitterPath.sitterServices();
    }
    if (uri.pathSegments.first == 'sitter-profile') {
      return BuddySitterPath.sitterProfile();
    }
    if (uri.pathSegments.first == 'pet-profile') {
      return BuddySitterPath.petProfile();
    }
    if (uri.pathSegments.first == 'config-sitter-profile') {
      return BuddySitterPath.configSitterProfile();
    }
    if (uri.pathSegments.first == 'config-pet-profile') {
      return BuddySitterPath.configPetProfile();
    }
    if (uri.pathSegments.first == 'add-pet') {
      return BuddySitterPath.addPet();
    }
    if (uri.pathSegments.first == 'sitter-info') {
      return BuddySitterPath.sitterInfo();
    }
    if (uri.pathSegments.first == 'pet-info') {
      return BuddySitterPath.petInfo();
    }
    // path: 404
    return BuddySitterPath.unknown();
  }
